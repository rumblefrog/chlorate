function generateIdFromHash(hash) {
    return hash.reduce((output, elem) => (output + (elem.toString(16)).slice(-2)), '')
        .split('')
        .map(elem => String.fromCharCode(97 + parseInt(elem, 16)))
        .reduce((output, elem) => output + elem, '')
        .slice(0, 32);
}

